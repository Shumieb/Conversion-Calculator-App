// kilogram conversions
export const KilogramsToGrams = (kilograms) => {
    return kilograms * 1000;
}

export const KilogramsToMiligrams = (kilograms) => {
    return kilograms * 1000000;
}

export const KilogramsToPounds = (kilograms) => {
    return kilograms * 2.2046;
}

export const KilogramsToOunce = (kilograms) => {
    return kilograms * 35.274;
}

// gram conversions
export const GramsToKilograms = (grams) => {
    return grams / 1000;
}

export const GramsToMiligrams = (grams) => {
    return grams * 1000;
}

export const GramsToPounds = (grams) => {
    return grams / 453.6;
}

export const GramsToOunce = (grams) => {
    return grams / 28.35;
}

