// km conversions
export const KilometersToMeters = (kilometers: number) => {
    return kilometers * 1000;
}

export const KilometersToCentimeters = (kilometers: number) => {
    return kilometers * 100000;
}

export const KilometersToMilimeters = (kilometers: number) => {
    return kilometers * 1000000;
}

// m conversions
export const MetersToKilometers = (meters: number) => {
    return meters / 1000;
}

export const MetersToCentimeters = (meters: number) => {
    return meters * 100;
}

export const MetersToMilimeters = (meters: number) => {
    return meters * 1000;
}

//cm conversions
export const CentimetersToKilometers = (centimeters: number) => {
    return centimeters / 100000;
}

export const CentimetersToMeters = (centimeters: number) => {
    return centimeters / 100;
}

export const CentimetersToMilimeters = (centimeters: number) => {
    return centimeters * 10;
}

// mm conversions
export const MilimetersToKilometers = (milimeters: number) => {
    return milimeters / 1000000;
}

export const MilimetersToMeters = (milimeters: number) => {
    return milimeters / 1000;
}

export const MilimetersToCentimeters = (centimeters: number) => {
    return centimeters / 10;
}