// kilogram conversions
export const KilogramsToGrams = (kilograms: number) => {
    return kilograms * 1000;
}

export const KilogramsToMiligrams = (kilograms: number) => {
    return kilograms * 1000000;
}

export const KilogramsToPounds = (kilograms: number) => {
    return kilograms * 2.2046;
}

export const KilogramsToOunce = (kilograms: number) => {
    return kilograms * 35.274;
}

// gram conversions
export const GramsToKilograms = (grams: number) => {
    return grams / 1000;
}

export const GramsToMiligrams = (grams: number) => {
    return grams * 1000;
}

export const GramsToPounds = (grams: number) => {
    return grams / 453.6;
}

export const GramsToOunce = (grams: number) => {
    return grams / 28.35;
}

// miligram conversions
